package programs;

public class IncAndDec3 {

	public static void main(String[] args) {
		
		
		int x = 5, y;
		y = ++x;
		System.out.println("y : " + y); 
		System.out.println("x : " + x); 
		
		y = x++;
		System.out.println("y : " + y); 
		System.out.println("x : " + x); 
		 
		++x;
		System.out.println("x : " + x); 
		 
		x++;
		System.out.println("x : " + x); 

	}

}

package programs;

public class FirstProgram {

	public static void main(String[] args) {
		
		
		int i=5;
		System.out.println(i);
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i);
		System.out.println(--i);
		System.out.println(i);
		System.out.println(i--);
		System.out.println(i);

	}

}

package operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		
		int a=5;
		int b=6;
		
		int c=a & b;
		
		System.out.println(c);
		
		int d=44;
		int e=66;
		
		int f=d|e;
		System.out.println(f);
		
		int g=b>>1;
		
		System.out.println(g);
		
		int h=b<<2;
		
		System.out.println(h);
		
		

	}

}

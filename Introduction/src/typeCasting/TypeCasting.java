		short i=6;
		int j=i;
		System.out.println(j);
		
		short k=3;
		long l=k;
		System.out.println(l);
		
		short m=1;
		float n=m;
		System.out.println(n);
		
		short o=8;
		double p=o;
		System.out.println(p);
		
		int q=3;
		long r=q;
		System.out.println(r);
		
